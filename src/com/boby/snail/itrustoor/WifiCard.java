package com.boby.snail.itrustoor;

public class WifiCard {

	private String macid = null;
	private String macname = null;
	private String mac = null;

	public void setmacid(String schid) {
		this.macid = schid;
	}

	public String getmacid() {
		return macid;
	}

	public void setmacname(String school_name) {
		this.macname = school_name;
	}

	public String getmacname() {
		return macname;
	}

	public void setmacs(String card) {
		this.mac = card;
	}

	public String getmacs() {
		return mac;
	}

}
